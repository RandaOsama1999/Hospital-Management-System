public class UnitMedication{
   private int Pill;
   private int Tape;
   private int bottleMliter;

   UnitMedication()
   {
       
   }
    public int getPill() {
        return Pill;
    }

    public void setPill(int Pill) {
        this.Pill = Pill;
    }

    public int getTape() {
        return Tape;
    }

    public void setTape(int Tape) {
        this.Tape = Tape;
    }

    public int getBottleMliter() {
        return bottleMliter;
    }

    public void setBottleMliter(int bottleMliter) {
        this.bottleMliter = bottleMliter;
    }
    
}
